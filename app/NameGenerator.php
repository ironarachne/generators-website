    public function generate($gender, $withLastName = true)
    {
        $first = $this->femaleFirstNames;
        $last = $this->femaleLastNames;

        if ($gender == 'male') {
            $first = $this->maleFirstNames;
            $last = $this->maleLastNames;
        }

        $name = random_item($first);

        if ($withLastName) {
            $name .= ' ' . random_item($last);
        }

        return $name;
    }

